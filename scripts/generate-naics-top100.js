/**
 * Generate src/data/naics-top100.ts from contractors.json + naics-codes.json.
 *
 * Aggregates total contract value per 6-digit NAICS across every contractor
 * row that lists it (codes can be comma-separated, so one row contributes
 * to multiple codes). Sorts desc, takes top 100.
 *
 * Run from project root: node scripts/generate-naics-top100.js
 */
const fs = require('fs');
const path = require('path');

const contractors = require('../src/data/contractors.json');
const naicsData = require('../src/data/naics-codes.json');

const naicsStats = {};

for (const row of contractors) {
  if (!row.naics) continue;
  const codeList = String(row.naics).split(',').map((c) => c.trim()).filter(Boolean);
  const value = Number(row.contract_value_num) || 0;
  for (const code of codeList) {
    if (!/^\d{6}$/.test(code)) continue;
    if (!naicsStats[code]) {
      naicsStats[code] = {
        totalValue: 0,
        contractorCount: 0,
        contractCount: 0,
        contractors: [],
        agencies: {},
      };
    }
    naicsStats[code].totalValue += value;
    naicsStats[code].contractorCount += 1;
    naicsStats[code].contractCount += Number(row.contract_count) || 0;
    naicsStats[code].contractors.push({ company: row.company, value });
    if (row.agencies) {
      for (const ag of String(row.agencies)
        .split(',')
        .map((a) => a.trim())
        .filter(Boolean)) {
        naicsStats[code].agencies[ag] = (naicsStats[code].agencies[ag] || 0) + value;
      }
    }
  }
}

const codes = naicsData.codes;
const ranked = Object.entries(naicsStats)
  .filter(([code]) => codes[code] && codes[code].title)
  .map(([code, stats]) => {
    const seen = new Set();
    const topContractors = stats.contractors
      .sort((a, b) => b.value - a.value)
      .filter((c) => {
        const key = c.company.toUpperCase().replace(/\s+/g, ' ').trim();
        if (seen.has(key)) return false;
        seen.add(key);
        return true;
      })
      .slice(0, 8)
      .map((c) => ({ company: c.company, value: c.value }));

    const topAgencies = Object.entries(stats.agencies)
      .sort((a, b) => b[1] - a[1])
      .slice(0, 5)
      .map(([name, value]) => ({ name, value }));

    return {
      code,
      title: codes[code].title,
      parent: codes[code].parent || null,
      totalValue: stats.totalValue,
      contractorCount: stats.contractorCount,
      contractCount: stats.contractCount,
      topContractors,
      topAgencies,
    };
  })
  .sort((a, b) => b.totalValue - a.totalValue)
  .slice(0, 100);

const header = `/**
 * Top 100 NAICS codes by federal contracting spend.
 *
 * Derived from contractors.json by aggregating total_contract_value
 * across every contractor row that lists each NAICS (codes can be
 * comma-separated, so a single contractor can contribute to multiple).
 *
 * Auto-generated by scripts/generate-naics-top100.js.
 * Regenerate when contractors.json updates.
 *
 * Used by:
 *  - /naics                index page (lists all 100, sorted by spend)
 *  - /naics/[code]         per-code SEO landing pages
 *  - sitemap.ts            emits one entry per code at priority 0.5
 */
export interface NaicsTopContractor {
  company: string;
  /** Total contract value attributed to this contractor (USD). May
   *  include awards across multiple NAICS codes since contractors.json
   *  rolls value up per row, not per code. */
  value: number;
}

export interface NaicsTopAgency {
  /** Agency name as it appears in contractors.json. */
  name: string;
  /** Total contract value awarded to contractors in this NAICS by
   *  this agency. Sum of contractor.value where the row lists this
   *  agency among its agencies field. */
  value: number;
}

export interface NaicsTopEntry {
  /** 6-digit NAICS code. */
  code: string;
  /** Official NAICS title from USASpending NAICS API. */
  title: string;
  /** 4-digit parent code (for the related-NAICS sibling lookup). */
  parent: string | null;
  /** Total federal contract spend across all contractors with this NAICS. */
  totalValue: number;
  /** Number of contractor rows that list this NAICS. */
  contractorCount: number;
  /** Aggregate contract count across contractors with this NAICS. */
  contractCount: number;
  /** Top contractors (by value), deduped by normalized company name. */
  topContractors: NaicsTopContractor[];
  /** Top agencies that procure under this NAICS. */
  topAgencies: NaicsTopAgency[];
}

export const NAICS_TOP_100: NaicsTopEntry[] = `;

const footer = `;

/**
 * Lookup by code. Returns undefined if the code is not in the top 100,
 * which is the signal /naics/[code] uses to 404 unknown codes.
 */
export function getNaicsTopEntry(code: string): NaicsTopEntry | undefined {
  return NAICS_TOP_100.find((e) => e.code === code);
}

/**
 * Related codes — same 4-digit parent prefix, excluding self. Used
 * on /naics/[code] to surface sibling industries inside the top 100.
 */
export function getRelatedNaics(code: string, limit = 5): NaicsTopEntry[] {
  const entry = getNaicsTopEntry(code);
  if (!entry || !entry.parent) return [];
  return NAICS_TOP_100.filter(
    (e) => e.parent === entry.parent && e.code !== code,
  ).slice(0, limit);
}
`;

const out = header + JSON.stringify(ranked, null, 2) + footer;
const target = path.join(__dirname, '..', 'src', 'data', 'naics-top100.ts');
fs.writeFileSync(target, out);
console.log(`Wrote ${target} (${out.length} bytes, ${ranked.length} entries)`);
